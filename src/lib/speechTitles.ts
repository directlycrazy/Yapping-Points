export function pickThree() {
    const titles = [];

    for (let i = 0; i < 3; i++) {
        let index = Math.floor(Math.random() * speechTitles.length);
        speechTitles.splice(index, 1);
        titles.push(speechTitles[index]);
    }

    return titles;
}

